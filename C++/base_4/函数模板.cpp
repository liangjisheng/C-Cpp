#include"iostream"
using namespace std;
template<typename r>r add(r a,r b)
{return a+b;}
int main()
{
	cout<<"int_add="<<add(10,20)<<endl;
	cout<<"double_add="<<add(10.2,20.4)<<endl;
	cout<<"char_add="<<add('A','\2')<<endl;
	cout<<"int_add="<<add(100,200)<<endl;
	return 0;
}

